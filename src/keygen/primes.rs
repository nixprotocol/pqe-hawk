    #[test]
    fn all_primes_in_range() {
        // All entries should be 31-bit primes between 2^30 and 2^31
        // (except the sentinel entry which is all zeros).
        for (i, sp) in PRIMES.iter().enumerate() {
            if sp.p == 0 {
                // Sentinel entry; skip range check.
                continue;
            }
            assert!(sp.p >= (1 << 30), "entry {} p too small: {}", i, sp.p);
            assert!(sp.p < (1 << 31), "entry {} p too large: {}", i, sp.p);
        }
    }
