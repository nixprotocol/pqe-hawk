/// Linear combination: `(u*f - v*g)/R mod p`.
/// `f` and `g` can be signed values (|x| <= 2^30) stored as u32 (two's complement).
///
/// Port of `mp_lin` (hawk_vrfy.c:44-58).
#[inline]
pub fn mp_lin(u: u32, v: u32, f: u32, g: u32, p: u32, p0i: u32) -> u32 {
    use crate::keygen::mp31::{mp_montymul, mp_sub, tbmask};
    let sf = tbmask(f);
    let f = (f ^ sf).wrapping_sub(sf);
    let sg = tbmask(g);
    let g = (g ^ sg).wrapping_sub(sg);
    let u = u.wrapping_add(sf & (p.wrapping_sub(u << 1)));
    let v = v.wrapping_add(sg & (p.wrapping_sub(v << 1)));
    mp_sub(mp_montymul(u, f, p, p0i), mp_montymul(v, g, p, p0i), p)
}

/// Modular division: `x/y mod p`. If `y` isn't invertible, returns 0.
///
/// `m16` is the Montgomery representation of 16 (i.e. `16 * R mod p`),
/// precomputed per-prime.
///
/// Port of `mp_div` (hawk_vrfy.c:64-171). Uses an optimized binary GCD
/// with update factors aggregated every 30 iterations.
pub fn mp_div(x: u32, y: u32, p: u32, p0i: u32, m16: u32) -> u32 {
    use crate::keygen::mp31::{mp_montymul, tbmask};
    let mut a = y;
    let mut b = p;
    let mut u = x;
    let mut v: u32 = 0;
    let mut af0: u32 = 0;
    let mut ag0: u32 = 0;
    let mut af1: u32 = 0;
    let mut ag1: u32 = 0;
    for i in 0..4 {
        let mut fg0: u32 = 1;
        let mut fg1: u32 = 1 << 16;
        for _ in 0..15 {
            let a_odd = (a & 1).wrapping_neg();
            let swap = tbmask(a.wrapping_sub(b)) & a_odd;
            let t1 = swap & (a ^ b);
            a ^= t1;
            b ^= t1;
            let t2 = swap & (fg0 ^ fg1);
            fg0 ^= t2;
            fg1 ^= t2;
            a = a.wrapping_sub(a_odd & b);
            fg0 = fg0.wrapping_sub(a_odd & fg1);
            a >>= 1;
            fg1 <<= 1;
        }
        fg0 = fg0.wrapping_add(0x7FFF_7FFF);
        fg1 = fg1.wrapping_add(0x7FFF_7FFF);
        let f0 = (fg0 & 0xFFFF).wrapping_sub(0x7FFF);
        let g0 = 0x7FFFu32.wrapping_sub(fg0 >> 16);
        let f1 = (fg1 & 0xFFFF).wrapping_sub(0x7FFF);
        let g1 = 0x7FFFu32.wrapping_sub(fg1 >> 16);

        if (i & 1) == 0 {
            af0 = f0;
            ag0 = g0;
            af1 = f1;
            ag1 = g1;
        } else {
            let bf0 = af0.wrapping_mul(f0).wrapping_sub(af1.wrapping_mul(g0));
            let bg0 = ag0.wrapping_mul(f0).wrapping_sub(ag1.wrapping_mul(g0));
            let bf1 = af0.wrapping_mul(f1).wrapping_sub(af1.wrapping_mul(g1));
            let bg1 = ag0.wrapping_mul(f1).wrapping_sub(ag1.wrapping_mul(g1));
            let nu = mp_lin(u, v, bf0, bg0, p, p0i);
            let nv = mp_lin(u, v, bf1, bg1, p, p0i);
            u = nu;
            v = nv;
        }
    }
    v = mp_montymul(v, m16, p, p0i);
    v & tbmask(b.wrapping_sub(2))
}

/// NTT variant for auto-adjoint polynomials. Only the first `n/2` elements
/// of `a` are used/modified.
///
/// Port of `mp_NTT_autoadj` (hawk_vrfy.c:208-242).
pub fn mp_ntt_autoadj(logn: u32, a: &mut [u32], gm: &[u32], p: u32, p0i: u32) {
    use crate::keygen::mp31::{mp_add, mp_montymul, mp_sub};
    let hn = 1usize << (logn - 1);
    let s1 = gm[1];
    let qn = hn >> 1;
    for u in 1..qn {
        let x1 = a[u];
        let x2 = a[hn - u];
        a[u] = mp_sub(x1, mp_montymul(x2, s1, p, p0i), p);
        a[hn - u] = mp_sub(x2, mp_montymul(x1, s1, p, p0i), p);
    }
    a[qn] = mp_sub(a[qn], mp_montymul(a[qn], s1, p, p0i), p);

    let mut t = 1usize << (logn - 1);
    for lm in 1..logn {
        let m = 1usize << lm;
        let ht = t >> 1;
        let mut v0 = 0usize;
        for u in 0..(m >> 1) {
            let s = gm[u + m];
            for v in 0..ht {
                let k1 = v0 + v;
                let k2 = k1 + ht;
                let x1 = a[k1];
                let x2 = mp_montymul(a[k2], s, p, p0i);
                a[k1] = mp_add(x1, x2, p);
                a[k2] = mp_sub(x1, x2, p);
            }
            v0 += t;
        }
        t = ht;
    }
}

/// Convert an i16 polynomial to the `[0, p)` representation, then NTT.
/// Port of `mp_poly_to_NTT` (hawk_vrfy.c:624-633).
pub fn mp_poly_to_ntt(logn: u32, d: &mut [u32], a: &[i16], p: u32, p0i: u32, gm: &[u32]) {
    use crate::keygen::mp31::{mp_ntt, tbmask};
    let n = 1usize << logn;
    debug_assert!(d.len() >= n);
    debug_assert!(a.len() >= n);
    for u in 0..n {
        let x = a[u] as i32 as u32;
        d[u] = x.wrapping_add(p & tbmask(x));
    }
    mp_ntt(logn, d, gm, p, p0i);
}

/// Like `mp_poly_to_ntt` but for an auto-adjoint polynomial (half-size writes).
/// Port of `mp_poly_to_NTT_autoadj` (hawk_vrfy.c:644-653).
pub fn mp_poly_to_ntt_autoadj(logn: u32, d: &mut [u32], a: &[i16], p: u32, p0i: u32, gm: &[u32]) {
    use crate::keygen::mp31::tbmask;
    let hn = 1usize << (logn - 1);
    debug_assert!(d.len() >= hn);
    debug_assert!(a.len() >= hn);
    for u in 0..hn {
        let x = a[u] as i32 as u32;
        d[u] = x.wrapping_add(p & tbmask(x));
    }
    mp_ntt_autoadj(logn, d, gm, p, p0i);
}

